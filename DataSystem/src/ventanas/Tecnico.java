    public Tecnico() {
        initComponents();
        user = Login.user;
        session_user = Administrador.session_user;

        setSize(550, 300);
        setResizable(false);
        setTitle("Tecnico - Sesi√≥n de " + user);
        setLocationRelativeTo(null);

        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icon = new ImageIcon(wallpaper.getImage().getScaledInstance(label_wallpaper.getWidth(),
                label_wallpaper.getHeight(), Image.SCALE_DEFAULT));
        label_wallpaper.setIcon(icon);
        this.repaint();

        if (session_user == 1) {
            setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        } else {
            setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);
        }

        try {
            Connection cn = Conexion.conect();
            PreparedStatement pst = cn.prepareStatement(
                    "select nombre_usuario from usuarios where username = '" + user + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                name_user = rs.getString("nombre_usuario");
                label_userName.setText("Bienvenido " + name_user);
            }

        } catch (SQLException e) {
            System.out.println("ERROR en consultar nombre del Tecnico.");
        }
    }

