    private void btn_accessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_accessActionPerformed
        user = txt_user.getText().trim();
        password = txt_password.getText().trim();
        
        if (!user.equals("") || !password.equals("")) {
            try {
                Connection cn = Conexion.conect();
                PreparedStatement pst = cn.prepareStatement(
                    "select tipo_nivel, status from usuarios where  username = '" + user
                        + "' and password = '" + password + "'");
                
                ResultSet rs = pst.executeQuery();
                
                if (rs.next()) {
                    String level_type = rs.getString("tipo_nivel");
                    String status = rs.getString("status");
                    
                    if (level_type.equalsIgnoreCase("Administrador") && status.equalsIgnoreCase("Activo")) {
                        dispose();
                        new Administrador().setVisible(true);
                    } else if(level_type.equalsIgnoreCase("Capturista") && status.equalsIgnoreCase("Activo")){
                        dispose();
                        new Capturista().setVisible(true);
                    } else if(level_type.equalsIgnoreCase("Tecnico") && status.equalsIgnoreCase("Activo")){
                        dispose();
                        new Tecnico().setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Datos de acceso incorrectos.");
                    txt_user.setText("");
                    txt_password.setText("");
                }
            } catch (SQLException e) {
                System.err.print("ERROR en el boton Acceder." + e);
                JOptionPane.showMessageDialog(null, "Â¡ERROR al iniciar sesion! contacte al administrador.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe de llenar todos los campos.");
        }
    }//GEN-LAST:event_btn_accessActionPerformed

