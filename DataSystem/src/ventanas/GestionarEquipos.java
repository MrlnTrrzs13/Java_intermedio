    public void GetDataTable() {
        table_teams.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int row_point = table_teams.rowAtPoint(e.getPoint());
                int column_point = 0;

                if (row_point > -1) {
                    id_teamUp = (int) model.getValueAt(row_point, column_point);
                    InformacionEquipoTecnico information = new InformacionEquipoTecnico();
                    information.setVisible(true);
                }
            }
        });
    }
