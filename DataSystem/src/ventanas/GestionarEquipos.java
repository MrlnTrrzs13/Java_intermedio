    private void btn_showActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_showActionPerformed
        String selection = cmb_status.getSelectedItem().toString();
        String query = "";

        model.setRowCount(0);
        model.setColumnCount(0);

        try {
            Connection cn = Conexion.conect();

            if (selection.equals("Todos")) {
                query = "select id_equipo, tipo_equipo, marca, status from equipos";
            } else {
                query = "select id_equipo, tipo_equipo, marca, status from equipos where status = '" + selection + "'";
            }
            PreparedStatement pst = cn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();

            table_teams = new JTable(model);
            jScrollPane1.setViewportView(table_teams);

            model.addColumn(" ");
            model.addColumn("Tipo");
            model.addColumn("Marca");
            model.addColumn("Estatus");

            while (rs.next()) {
                Object[] row = new Object[4];
                for (int i = 0; i < 4; i++) {
                    row[i] = rs.getObject(i + 1);
                }
                model.addRow(row);
            }
            cn.close();

        } catch (SQLException e) {
            System.err.println("ERROR al recuperar los registros de equipos " + e);
        }
        GetDataTable();
    }//GEN-LAST:event_btn_showActionPerformed

