    public boolean ValidateExpression() {
        Pila stack = new Pila();
        String cadena = texto.getText();

        for (int i = 0; i < cadena.length(); i++) {
            if (cadena.charAt(i) == '(' || cadena.charAt(i) == '[' || cadena.charAt(i) == '{') {
                stack.Insert(cadena.charAt(i));
            } else {
                if (cadena.charAt(i) == ')') {
                    if (stack.extract() != '(') {
                        return false;
                    }

                } else {
                    if (cadena.charAt(i) == ']') {
                        if (stack.extract() != '[') {
                            return false;
                        }

                    } else {
                        if (cadena.charAt(i) == '}') {
                            if (stack.extract() != '{') {
                                return false;
                            }

                        }

                    }
                }
            }
        }
        return stack.EmptyStack();
    }
