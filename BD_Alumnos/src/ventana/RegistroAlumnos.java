    private void btn_pdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pdfActionPerformed
        Document document = new Document();

        try {
            String ruta = System.getProperty("user.home");
            PdfWriter.getInstance(document, new FileOutputStream(ruta + "/OneDrive/Reporte_Alumnos.pdf"));
            Image header = Image.getInstance("src/img/header.png");
            header.scaleToFit(650, 1000);
            header.setAlignment(Chunk.ALIGN_CENTER);
            
            Paragraph paragraph = new Paragraph();
            paragraph.setAlignment(Paragraph.ALIGN_CENTER);
            paragraph.add("Formato creado por la Brandon \n\n");
            paragraph.setFont(FontFactory.getFont("Tahoma", 10, Font.BOLD, BaseColor.DARK_GRAY));
            paragraph.add("Alumnos registrados \n\n");
                    
            document.open();
            document.add(header);
            document.add(paragraph);

            PdfPTable table = new PdfPTable(3);
            table.addCell("Codigo");
            table.addCell("Nombre del Alumno");
            table.addCell("Grupo");

            try {
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_ins", "root", "");
                PreparedStatement pst = cn.prepareStatement("select * from alumnos");
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    do {
                        table.addCell(rs.getString(1));
                        table.addCell(rs.getString(2));
                        table.addCell(rs.getString(3));
                    } while (rs.next());
                    document.add(table);
                }
            } catch (DocumentException | SQLException e) {

            }
            document.close();
            JOptionPane.showMessageDialog(null, "Reporte creado.");
        } catch (DocumentException | HeadlessException | FileNotFoundException  e) {
            System.out.println("Error en PDF" + e);
        } catch (IOException e) {
            System.out.println("Errot en la Imagen" + e);
        }
    }//GEN-LAST:event_btn_pdfActionPerformed

