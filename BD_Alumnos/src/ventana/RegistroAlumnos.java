    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_ins", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from alumnos where ID = ?");

            pst.setString(1, txt_search.getText().trim());

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txt_name.setText(rs.getString("NombreAlumno"));
                txt_group.setText(rs.getString("Grupo"));
            } else {
                JOptionPane.showMessageDialog(null, "Alumno no registrado");
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btn_searchActionPerformed

